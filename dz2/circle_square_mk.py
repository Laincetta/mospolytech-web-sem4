import random
import math

def circle_square_mk(r, n):
    """
    Вычисляет площадь круга методом Монте-Карло.
    r - радиус, n - количество случайных точек.
    """
    inside_circle = 0
    
    for _ in range(n):
        # Генерируем случайную точку (x, y) в квадрате со стороной 2r.
        # Центр круга в (0, 0), координаты x и y от -r до r.
        x = random.uniform(-r, r)
        y = random.uniform(-r, r)
        
        # Проверяем, попала ли точка внутрь круга (x^2 + y^2 <= r^2)
        if x**2 + y**2 <= r**2:
            inside_circle += 1
            
    # Площадь квадрата вокруг круга = (2r) * (2r) = 4 * r^2
    square_area = 4 * (r**2)
    
    # Относительная площадь круга: (попавшие точки / всего точек) * площадь квадрата
    return (inside_circle / n) * square_area

if __name__ == '__main__':
    r = 1.0
    # Тестируем для разных n
    for n in [100, 10000, 1000000]:
        mk_area = circle_square_mk(r, n)
        math_area = math.pi * (r**2)
        error = abs(mk_area - math_area)
        
        print(f"n = {n:7} | МК: {mk_area:.5f} | Формула: {math_area:.5f} | Ошибка: {error:.5f}")

